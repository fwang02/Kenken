package Domain;

/**
 * Esta clase se utiliza para guardar las posiciones de las casillas.
 * 
 * @author Javier Parcerisas
 */
public class Pos {
    public int posX;
    public int posY;

    /**
	 * Constructora básica de la clase Pos. 
     * 
     * @param x posición x
     * @param y posición y
	 */
    public Pos(int x, int y) {
        posX = x;
        posY = y;
    }
}
